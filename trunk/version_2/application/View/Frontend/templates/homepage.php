            <h1>Bet script v2.0 (Alpha)</h1>
            <p>Bet script v2.0 is simulation of betting software  licensed under GPL 2.0</p>
            <br />
	 <br />
            <h1>Features</h1>
            <p>All things you can find in sportsbook page except system bets.</p>
	<p>Its a service script not software script. It means script is betting server that serves betting data (JSON) or HTML. Design as service enables easy integration of script in third party software like Joomla or Wordpress.  In demo you will see no navigation through links, its because page is calling service. Service returns JSON data or HTML widgets</p>
	 <br />
	 <br />
	 <h1>Why Alpha?</h1>
	<p>Architecture of script is finished but its not heavily tested</p>
	<p>Security is not full tested, script is still open for sql injection attacks</p>
	<p>UI improvements after receiving feedback</p>
	<p>Some parts are not implemented yet like sorting engine in service</p>
	<br />
	 <br />
	<h1>Can I use Alpha on live site?</h1>
	<p>Our suggestion is no, but in beta version all security issues will be solved, database changes are not in any plans so you can start working with script and upgrade when beta in finished</p> 
